#include <stdio.h>

int main()
{
	printf("Uma cor: %s\n", "verde");
	printf("Numero: %d\n", 12345);
	printf("Numero com zeros: %04d\n", 25);
	printf("Outro numero: %i\n", 1234);
	printf("Decimal com 3 digitos: %3.2f\n", 3.14159);
	printf("Hexadecimal: %x\n", 255);
	printf("Octal: %o\n", 255);
	printf("Valor sem sinal: %u\n", -150);
	printf("Imprimindo o simbolo %%\n");
	
	return 0;
}



