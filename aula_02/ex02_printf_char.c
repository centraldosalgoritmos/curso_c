#include <stdio.h>

int main()
{

	// imprimindo caracter
	printf("Letra: %c\n", 'a');
	printf("Letra: %c\n", 'A');
	printf("Digito: %c\n", '0');
	printf("Digito: %c\n", '9');

	printf("Imprimindo o simbolo: %%\n");

	return 0;
}
